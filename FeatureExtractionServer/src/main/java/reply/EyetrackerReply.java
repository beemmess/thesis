package reply;

public class EyetrackerReply {


    private String raw;
    private String interpolate;
    private String avgPupil;
    private String substitution;

    public String getRaw() {
        return raw;
    }

    public void setRaw(String raw) {
        this.raw = raw;
    }

    public String getInterpolate() {
        return interpolate;
    }

    public void setInterpolate(String interpolate) {
        this.interpolate = interpolate;
    }

    public String getAvgPupil() {
        return avgPupil;
    }

    public void setAvgPupil(String avgPupil) {
        this.avgPupil = avgPupil;
    }

    public String getSubstitution() {
        return substitution;
    }

    public void setSubstitution(String substitution) {
        this.substitution = substitution;
    }
}
